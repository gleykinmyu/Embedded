// =============================================================================
// Таксономия транспортов (ориентир для развития lib/Interfaces)
// =============================================================================
//
// IByteStream — упорядоченные байты. База для протоколов и обёрток:
//   • RS-485: класс-обёртка над IByteStream (UART) + управление DE/RE + при необходимости drain TX.
//   • Мост SLIP/COBS поверх любого IByteStream.
//
// Отдельные корневые интерфейсы (не наследовать от IByteStream «в лоб»):
//   • CAN — приём/передача кадров (id, RTR, длина, данные).
//   • I2C — транзакции (адрес, запись, повторный старт, чтение).
//   • SPI — кадры / обмен блоками с CS и режимом clock.
//   • DMX512 — break, MAB, слот-тайминг; это не семантика обычного read/write.
//
// Связка слоёв: протоколы верхнего уровня зависят от минимального контракта (IByteStream или
// ICanBus и т.д.). Адаптеры (например «упаковать CAN в поток для лога») — отдельные классы.
// =============================================================================
